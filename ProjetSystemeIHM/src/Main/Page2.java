    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
        int n = Integer.parseInt(txt1.getText());
        
        tab = new Processus[n];
        
         DefaultTableModel dtm = new DefaultTableModel(0, 0);

        // add header of the table
        String header[] = new String[] { "Num Client", "Check IN", 
            "Dur√©e de sejour","Etat"};

        // add header in table model     
         dtm.setColumnIdentifiers(header);
            //set model into the table object
               table.setModel(dtm);
               JSONArray processusTab = new JSONArray();
               Random rand = new Random();
             // add row dynamically into the table      
        for (int count = 1; count <= n; count++) {
            String nom = "P"+count;
            int temp = rand.nextInt(10);  
            int duree = rand.nextInt(10)+1;
            int priorite = rand.nextInt(4);
        
            JSONObject unProcessus = new JSONObject();
                try {
                unProcessus.put("nom", nom);
                unProcessus.put("dureeEx", duree);
                unProcessus.put("tempArr", temp);
                unProcessus.put("priorite", priorite);
                processusTab.put(unProcessus);
                } catch (Exception e) { }
            
            tab[count-1]=new Processus(nom,duree,priorite,temp);//objet
            dtm.addRow(new Object[] { nom,temp,duree, priorite });
         }
        
        
        try {
            JSONObject processus = new JSONObject();
            processus.put("Processus", processusTab);
            FileWriter file = new FileWriter("C:\\Users\\Thamer\\Desktop\\"
                    + "1_ISEOC\\projets 1ING\\SE\\ProjetSystemeIHM\\src\\"
                    + "Processus.json");
            file.write(processus.toString());
            file.flush();
        } catch (Exception e) {
        }
        
         fifobtn.setEnabled(true);
        
        }
        catch(Exception e){
            showMessageDialog(null, "Nombre processus doit etre entier");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

